def utc_now_iso() -> str:
    """Return the current UTC timestamp."""

    return datetime.now(timezone.utc).isoformat()


def load_env_file(path: Path) -> dict[str, str]:
    """Load a minimal .env file without extra dependencies."""

    if not path.exists():
        raise FileNotFoundError(
            f"No se encontro {path}. Crea tu .env a partir de .env_example antes de subir videos."
        )

    env: dict[str, str] = {}
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" not in line:
            continue
        key, value = line.split("=", 1)
        key = key.strip()
        value = value.strip()
        if value.startswith(("'", '"')) and value.endswith(("'", '"')) and len(value) >= 2:
            value = value[1:-1]
        env[key] = value
    return env


def env_get(env: dict[str, str], key: str, default: str | None = None) -> str | None:
    """Return an env value stripped, treating empty strings as missing."""

    value = env.get(key, default)
    if value is None:
        return None
    value = value.strip()
    return value or None


def env_get_bool(env: dict[str, str], key: str, default: bool = False) -> bool:
    """Parse a boolean env value."""

    value = env_get(env, key)
    if value is None:
        return default
    return value.lower() in {"1", "true", "yes", "on"}


def env_get_int(env: dict[str, str], key: str, default: int | None = None) -> int | None:
    """Parse an integer env value."""

    value = env_get(env, key)
    if value is None:
        return default
    return int(value)


def resolve_repo_path(value: str | None, fallback: Path | None = None) -> Path | None:
    """Resolve a path relative to the repository root."""

    if value is None:
        return fallback
    candidate = Path(value).expanduser()
    if not candidate.is_absolute():
        candidate = PROJECT_ROOT / candidate
    return candidate


