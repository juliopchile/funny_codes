def normalize_spaces(value: str | None) -> str:
    """Collapse whitespace in a string."""

    if not value:
        return ""
    return re.sub(r"\s+", " ", value).strip()


def sanitize_youtube_title(entry: dict[str, Any]) -> str:
    """Build a YouTube-safe title from the JSON entry."""

    base_title = (
        entry.get("demo_title")
        or entry.get("card_title")
        or Path(str(get_upload_video_path(entry) or "video")).stem
    )
    title = normalize_spaces(str(base_title))
    title = title.replace("<", "-").replace(">", "-")
    title = re.sub(r"[\x00-\x1F\x7F]", "", title)
    if len(title) > TITLE_MAX_CHARS:
        title = title[:TITLE_MAX_CHARS].rstrip()
    if not title:
        title = "Planview demo"
    return title


def trim_utf8_bytes(value: str, max_bytes: int) -> str:
    """Trim a string to a maximum UTF-8 byte length."""

    encoded = value.encode("utf-8")
    if len(encoded) <= max_bytes:
        return value
    trimmed = encoded[:max_bytes]
    while True:
        try:
            return trimmed.decode("utf-8").rstrip()
        except UnicodeDecodeError:
            trimmed = trimmed[:-1]


def sanitize_youtube_description(entry: dict[str, Any]) -> str:
    """Build a YouTube-safe description from the JSON entry."""

    description = entry.get("demo_description") or entry.get("card_summary") or ""
    description = str(description).replace("<", "-").replace(">", "-")
    description = re.sub(r"[\x00-\x08\x0B\x0C\x0E-\x1F\x7F]", "", description)
    description = description.strip()
    return trim_utf8_bytes(description, DESCRIPTION_MAX_BYTES)


def validate_privacy_status(value: str | None) -> str:
    """Validate the requested privacy status."""

