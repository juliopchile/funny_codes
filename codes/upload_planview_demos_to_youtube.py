"""
Upload Planview demo videos to YouTube using the official API.

Requirements
============

.. code-block:: bash

   pip install google-api-python-client google-auth-oauthlib google-auth-httplib2

Usage
=====

.. code-block:: bash

   # Option 1: edit RUN_LIMIT / RUN_DRY_RUN below and press Play in VS Code
   python codes/upload_planview_demos_to_youtube.py

   # Option 2: CLI
   python codes/upload_planview_demos_to_youtube.py
   python codes/upload_planview_demos_to_youtube.py --limit 3
   python codes/upload_planview_demos_to_youtube.py --dry-run

Notes
=====

This module is intentionally specific to the JSON files generated by
``codes/scrapping_planviews_demos.py`` and to the one-off upload flow
requested for Planview demos.
"""

